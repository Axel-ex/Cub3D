#include "../../includes/cub3D.h"

void	print_map(void)
{
	char	**map;
	int		i;
	int		j;

	map = game()->map;
	i = -1;
	while (map[++i])
	{
		j = -1;
		while (map[i][++j])
		{
			printf("%c", map[i][j]);
		}
		printf("\n");
	}
}
