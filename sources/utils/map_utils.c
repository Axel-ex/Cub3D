#include "../../includes/cub3D.h"

bool	is_player(char c)
{
	return (c == 'N' || c == 'S' || c == 'W' || c == 'E'
			|| c == PLAYER);
}

bool	is_floor(char c)
{
	return (c == FLOOR);
}

bool	is_wall(char c)
{
	return (c == WALL);
}
